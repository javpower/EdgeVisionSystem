package com.edge.vision.core.quality;

/**
 * 匹配策略枚举
 */
public enum MatchStrategy {
    /**
     * 拓扑图匹配
     * 优点：支持旋转、平移、尺度变化，适合复杂场景
     * 缺点：全局最优匹配，统计可能不准确
     */
    TOPOLOGY,

    /**
     * 坐标直接匹配
     * 优点：一对一关系明确，错检漏检一目了然
     * 缺点：对旋转、平移敏感，需要预先校正坐标
     */
    COORDINATE,

    /**
     * 四角匹配（Four-Corner Matching）
     * 优点：利用工件四角作为世界坐标系，通过仿射不变量（数字指纹）匹配
     *      支持平移、旋转、缩放，遮挡鲁棒性高
     * 缺点：需要预先标定工件四角坐标
     * 适用场景：工件有明显的四个角（或可贴标记），可能发生仿射变换
     */
    FOUR_CORNER
}
